package homework;

public class ProblemOne {
    public void Math(int firstNumber, int secondNumber){
        int sum = firstNumber + secondNumber;
        int substract = firstNumber - secondNumber;
        int multiply = firstNumber * secondNumber;
        int divide = firstNumber / secondNumber;
        int remainder = firstNumber % secondNumber;
        System.out.println(firstNumber + " + " + secondNumber + " = " + sum);
        System.out.println(firstNumber + " - " + secondNumber + " = " + substract);
        System.out.println(firstNumber + " * " + secondNumber + " = " + multiply);
        System.out.println(firstNumber + " / " + secondNumber + " = " + divide);
        System.out.println(firstNumber + " % " + secondNumber + " = " + remainder);
    }



}
